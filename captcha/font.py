    def __init__(self, dot_size: int = 4, dot_gap: int = 2, char_gap: int = 8) -> None:
        self.dot_size = dot_size
        self.dot_gap = dot_gap
        self.char_gap = char_gap

    @property
    def char_width(self) -> int:
        """Pixel width of a single glyph bounding box."""
        return self.GRID_COLS * (self.dot_size + self.dot_gap) - self.dot_gap

    @property
    def char_height(self) -> int:
        """Pixel height of a single glyph bounding box."""
        return self.GRID_ROWS * (self.dot_size + self.dot_gap) - self.dot_gap

    def measure(self, text: str) -> tuple[int, int]:
        """Return (width, height) in pixels for a string."""
        if not text:
            return (0, self.char_height)
        w = len(text) * (self.char_width + self.char_gap) - self.char_gap
        return (w, self.char_height)

    def draw_char(
        self, draw: ImageDraw.ImageDraw, char: str, x: int, y: int, color: tuple
    ) -> None:
        """Draw a single character with its top-left corner at (x, y)."""
        dots = self.GLYPHS.get(char.upper())
        if dots is None:
            return
        step = self.dot_size + self.dot_gap
        r = self.dot_size - 1
        for col, row in dots:
            dx = x + col * step
            dy = y + row * step
            if r == 0:
                draw.point((dx, dy), fill=color)
            else:
                draw.ellipse([dx, dy, dx + r, dy + r], fill=color)

    def draw_text(
        self, draw: ImageDraw.ImageDraw, text: str, x: int, y: int, color: tuple
    ) -> None:
        """Draw a string of characters starting at (x, y)."""
        advance = self.char_width + self.char_gap
        for i, char in enumerate(text.upper()):
            self.draw_char(draw, char, x + i * advance, y, color)

    def draw_text_centred(
        self,
        draw: ImageDraw.ImageDraw,
        text: str,
        canvas_width: int,
        y: int,
        color: tuple,
    ) -> None:
        """Draw a string horizontally centred within canvas_width."""
        w, _ = self.measure(text)
        self.draw_text(draw, text, (canvas_width - w) // 2, y, color)
